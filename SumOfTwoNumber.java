public class SumOfTwoNumbers {
    public static void main(String[] args) {
        // Declare and initialize two numbers
        int a = 5;
        int b = 10;

        // Calculate the sum
        int sum = a + b;

        // Print the result
        System.out.println("The sum of " + a + " and " + b + " is: " + sum);
    }
}
